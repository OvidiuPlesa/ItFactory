package com.itFactory;

public class HelloWorld {

    // public -> este un modificator de acces. Face ca metoda sa fie vizibila in tot proiectul. (Comentariu pe o singura linie)
    /*
    * static - nu avem nevoie de instanta/obiect pentru a apela aceasta metoda
    * void -> inseamna ca aceasta metoda nu returneaza nimic
    * ----> acesta este un comentariu pe mai multe linii
    *
    * */


    /**
     * main -> aceasa este metoda principala care porneste programul
     * fara aceasta metoda nu vom putea porni programul
     *  String[] args -> un array de tip String prezinta argumentele programului
     *
     */


    public static void main(String[] args) {
        System.out.println("Hello World");



    }

}
